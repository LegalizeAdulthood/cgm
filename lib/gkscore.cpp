void gks_seg_xform(double *x, double *y)
{
  double xx;

  xx = *x * gkss->mat[0][0] + *y * gkss->mat[0][1] + gkss->mat[2][0];
  *y = *x * gkss->mat[1][0] + *y * gkss->mat[1][1] + gkss->mat[2][1];
  *x = xx;
}

#ifdef _WIN32
void gks_perror(const char *format, ...)
{
    va_list ap;
    char s[BUFSIZ];

    va_start(ap, format);
    vsprintf(s, format, ap);
    va_end(ap);

    MessageBox(nullptr, s, "GKS", MB_OK | MB_ICONHAND);
}
#else
void gks_perror(const char *format, ...)
{
    va_list ap;

    if (gks_a_error_file == NULL)
        gks_a_error_file = stderr;

    fprintf(gks_a_error_file, "GKS: ");

    va_start(ap, format);
    vfprintf(gks_a_error_file, format, ap);
    va_end(ap);

    fprintf(gks_a_error_file, "\n");
}
#endif

char *gks_malloc(int size)
{
    char *temp;

    temp = (char *) calloc(1, size);
    if (temp == 0)
    {
        gks_fatal_error("gks_malloc: cannot allocate memory");
    }

    return (temp);
}

char *gks_realloc(void *ptr, int size)
{
    char *temp;

    temp = ptr ? (char *) realloc(ptr, size) : (char *) malloc(size);
    if (temp == 0)
    {
        gks_fatal_error("gks_realloc: cannot allocate memory");
    }

    return (temp);
}

/* Use this as the function to call when adding unwind protects so we
don't need to know what free() returns. */

void gks_free(void *ptr)
{
    if (ptr)
        free(ptr);
}

void gks_fatal_error(const char *args, ...)
{
    gks_perror(args);
    exit(-1);
}

